<template>
  
    <header class="header">
        <strong>Счетчик {{ counter }}</strong>
        <button class="btn" @click="add">Добавить 5</button>
    </header>
</template>

<script>
export default {
    computed: {
        counter() {
            return this.$store.state.counter
        }
    },
    methods: {
       add() {
           this.$store.commit('add', {
               value: 5
           })
       }  
    }

}
</script>

